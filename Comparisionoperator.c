// comparision operator
#include<stdio.h>
void main()
{
 int a=12,b=6,ans;
 ans=a<b;
 printf("%d < %d = %d \n",a,b,ans);
 ans=a>b;
 printf("%d > %d = %d \n",a,b,ans);
 ans=a<=b;
 printf("%d =< %d = %d \n",a,b,ans);
 ans=a>=b;
 printf("%d => %d = %d \n",a,b,ans);
 ans=a!=b;
 printf("%d != %d = %d \n",a,b,ans);
 ans=a==b;
 printf("%d == %d = %d \n",a,b,ans);
 	
}
